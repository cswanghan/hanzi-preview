// Get stroke count for a character
function getStrokeCount(char: string): number {
  if (strokeCountMap[char]) {
    return strokeCountMap[char]
  }

  // Estimate based on Unicode range
  const code = char.charCodeAt(0)
  if (code >= 0x4e00 && code <= 0x4e3f) return 1 + Math.floor((code - 0x4e00) / 16)
  if (code >= 0x4e40 && code <= 0x4e7f) return 2 + Math.floor((code - 0x4e40) / 16)
  if (code >= 0x4e80 && code <= 0x4ebf) return 3 + Math.floor((code - 0x4e80) / 16)
  if (code >= 0x4ec0 && code <= 0x4eff) return 4 + Math.floor((code - 0x4ec0) / 12)
  if (code >= 0x4f00 && code <= 0x4f3f) return 5 + Math.floor((code - 0x4f00) / 12)
  if (code >= 0x4f40 && code <= 0x4f7f) return 6 + Math.floor((code - 0x4f40) / 12)
  if (code >= 0x4f80 && code <= 0x4fbf) return 7 + Math.floor((code - 0x4f80) / 12)

  return 5
}

// Get words for a character
function getWords(char: string): string[] {
  return wordsByChar[char]?.slice(0, 8) || []
}

// Main generator function
export function generateHanziDetail(char: string): HanziDetail | null {
  if (!char || char.length !== 1) {
    return null
  }

  // Get pinyin using pinyin-pro
  const pinyinResult = pinyin(char, { toneType: "symbol" })

  if (!pinyinResult) {
    return null
  }

  // Detect if polyphonic
  const allPinyins = pinyin(char, { toneType: "symbol", multiple: true })
  const isPolyphonic = Array.isArray(allPinyins) && allPinyins.length > 1

  // Get stroke count
  const strokeCount = getStrokeCount(char)

  // Get words
  const words = getWords(char)

  return {
    char,
    pinyin: pinyinResult,
    audioText: char,
    words,
    isPolyphonic,
    strokeCount,
    radical: undefined,
  }
}

// Check if a character can be generated
export function canGenerateHanzi(char: string): boolean {
  if (!char || char.length !== 1) return false

  try {
    const result = pinyin(char, { toneType: "symbol" })
    return !!result
  } catch {
    return false
  }
}
